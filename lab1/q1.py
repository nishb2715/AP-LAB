#program to swap two variables with temp 
a = 5
b = 10          
temp = a
a = b
b = temp
print("After swapping with temp:")
print("a =", a) 
print("b =", b)




#program to swap two variables without temp
a = 5
b = 10
a = a + b
b = a - b
a = a - b
print("After swapping without temp:")
print("a =", a)
print("b =", b)

